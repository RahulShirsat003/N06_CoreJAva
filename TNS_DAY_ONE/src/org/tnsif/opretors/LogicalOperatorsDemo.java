//program for logical Operators

package org.tnsif.opretors;

public class LogicalOperatorsDemo {

	@SuppressWarnings("unused")
	public static void main(String[] args) {
		
		//logical AND
		System.out.println(31!=7 && 3>=5);
		
		//logical OR
		System.out.println(31!=7 || 3>=5);
		
		//logical Negation
		System.out.println(!(31!=7 || 3>=5));
		

	}

}
